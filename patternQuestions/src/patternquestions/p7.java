
package patternquestions;

public class p7 {
    public static void main(String args[]){
        for(int i=1;i<=3;i++){
            for(int j=1;j<=4;j++){
              if((i+j)%2==0){
                  System.out.print(1);
              }else{
               System.out.print(2);
            }
            }
            
            System.out.println();
        }
        
    }
    }


